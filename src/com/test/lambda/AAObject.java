public class AAObject {
	
	private int no;
	
	private String name;
	
	
	public AAObject(int no,String name){
		super();
		this.no = no;
		this.name = name;
	}

	public AAObject() {
		
	}

	public int getNo() {
		return no;
	}

	public void setNo(int no) {
		this.no = no;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
